#!/usr/bin/env node
/**
 * Comixtime scraper → Takobon SQL migration
 *
 * Usage:
 *   COMIXTIME_TOKEN='$2y$10$...' node scripts/scrape-comixtime.mjs
 *
 * Get the token from Chrome DevTools → Network → any codek.comixtime.it request
 * → Request Headers → cookie → authToken=<value>
 *
 * Output: supabase/migrations/007_comixtime_import.sql
 */

import { writeFileSync } from "fs";
import { join } from "path";

const BASE_API = "https://codek.comixtime.it/platform/V1";
const IMG_BASE = "https://fra1.digitaloceanspaces.com/comixtime/img/";

const EMAIL = process.env.COMIXTIME_EMAIL;
const PASSWORD = process.env.COMIXTIME_PASSWORD;

if (!EMAIL || !PASSWORD) {
  console.error("Set COMIXTIME_EMAIL and COMIXTIME_PASSWORD env vars.");
  process.exit(1);
}

const AUTH = "Basic " + Buffer.from(`${EMAIL}:${PASSWORD}`).toString("base64");

const headers = {
  "Content-Type": "application/json",
  "Authorization": AUTH,
  "Origin": "https://dex.comixtime.it",
  "Referer": "https://dex.comixtime.it/",
  "User-Agent": "Mozilla/5.0 (Linux; Android 6.0; Nexus 5 Build/MRA58N) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/147.0.0.0 Mobile Safari/537.36",
};

async function post(path, body) {
  const res = await fetch(`${BASE_API}${path}`, {
    method: "POST",
    headers,
    body: JSON.stringify(body),
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`POST ${path} → ${res.status}: ${text}`);
  }
  return res.json();
}

async function fetchAllPages(path, extraFilters = {}, pageSize = 50) {
  const items = [];
  let page = 1;
  let totalPages = 1;
  while (page <= totalPages) {
    const data = await post(path, {
      search: "",
      page,
      pageSize,
      filters: extraFilters,
      sort: {},
    });
    if (data.error) throw new Error(JSON.stringify(data.error));
    const results = data.data ?? data.comics ?? data.series ?? data.volumes ?? [];
    items.push(...results);
    totalPages = data.totalPages ?? 1;
    console.log(`  ${path} page ${page}/${totalPages} (${results.length} items)`);
    page++;
    // small delay to be polite
    await new Promise(r => setTimeout(r, 200));
  }
  return items;
}

function slugify(str) {
  return str
    .toLowerCase()
    .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function sqlStr(val) {
  if (val === null || val === undefined) return "NULL";
  return "'" + String(val).replace(/'/g, "''") + "'";
}

function coverUrl(multimedia) {
  if (!multimedia || multimedia.length === 0) return null;
  const cover = multimedia.find(m => m.type === "cover" || m.position === 1) ?? multimedia[0];
  const path = cover.url ?? cover.path ?? cover.filename;
  if (!path) return null;
  return IMG_BASE + path;
}

const genreMap = {
  "azione": "action",
  "avventura": "adventure",
  "fantascienza": "sci-fi",
  "fantasy": "fantasy",
  "horror": "horror",
  "romantico": "romance",
  "commedia": "comedy",
  "drammatico": "drama",
  "thriller": "thriller",
  "storico": "historical",
  "sportivo": "sports",
  "soprannaturale": "supernatural",
  "slice of life": "slice_of_life",
  "psicologico": "psychological",
  "mistero": "mystery",
};

function mapGenres(genres) {
  if (!genres || genres.length === 0) return "'{}'";
  const mapped = genres
    .map(g => genreMap[(g.name ?? g).toLowerCase()] ?? slugify(g.name ?? g))
    .filter(Boolean);
  if (mapped.length === 0) return "'{}'";
  return "ARRAY[" + mapped.map(sqlStr).join(", ") + "]";
}

// Italian publishers to include (fuzzy match)
const ITALIAN_PUBLISHERS = [
  "panini", "star comics", "jpop", "j-pop", "bao", "bonelli",
  "tunue", "tunué", "feltrinelli", "planeta", "magic press",
  "rw lion", "rw edizioni", "lion", "hikari", "dynit",
  "001 edizioni", "kappalab", "edizioni bd",
];

function isItalianPublisher(name) {
  if (!name) return false;
  const n = name.toLowerCase();
  return ITALIAN_PUBLISHERS.some(p => n.includes(p));
}

async function main() {
  console.log("📚 Fetching publishers…");
  let publishers;
  try {
    const data = await post("/publishers/paginated", {
      search: "", page: 1, pageSize: 100, filters: {}, sort: {},
    });
    publishers = data.data ?? [];
  } catch (e) {
    console.error("Auth failed or API error:", e.message);
    process.exit(1);
  }

  const italianPubs = publishers.filter(p => isItalianPublisher(p.name) || p.country === "IT");
  console.log(`Found ${italianPubs.length} Italian publishers out of ${publishers.length}`);

  const sql = [];
  sql.push("-- Takobon — Comixtime import");
  sql.push("-- Auto-generated by scripts/scrape-comixtime.mjs");
  sql.push("");
  sql.push("-- ─────────────────────────────────────────────");
  sql.push("-- PUBLISHERS");
  sql.push("-- ─────────────────────────────────────────────");
  sql.push("");

  for (const pub of italianPubs) {
    const slug = slugify(pub.name);
    const name = pub.name.replace(/'/g, "''");
    sql.push(`insert into publishers (name, country, slug) values ('${name}', 'IT', '${slug}') on conflict (slug) do nothing;`);
  }

  sql.push("");
  sql.push("-- ─────────────────────────────────────────────");
  sql.push("-- SERIES");
  sql.push("-- ─────────────────────────────────────────────");
  sql.push("");

  let totalVolumes = 0;
  const seriesInserted = [];

  for (const pub of italianPubs) {
    console.log(`\n📖 Fetching series for "${pub.name}"…`);
    let seriesList;
    try {
      seriesList = await fetchAllPages("/series/paginated", { publisher_id: pub.id }, 50);
    } catch (e) {
      console.warn(`  Skipping "${pub.name}": ${e.message}`);
      continue;
    }

    const pubSlug = slugify(pub.name);

    for (const s of seriesList) {
      const title = s.title ?? s.name ?? "";
      const titleIt = s.title_it ?? s.localized_title ?? title;
      const slug = slugify(title) || slugify(titleIt);
      if (!slug || !title) continue;

      // Determine type (manga vs comic)
      const type = s.type === "manga" || pub.name.toLowerCase().includes("jpop") || pub.name.toLowerCase().includes("j-pop") || pub.name.toLowerCase().includes("star comics")
        ? "manga"
        : "comic";

      const status = s.status === "completed" || s.status === "conclusa" ? "completed"
        : s.status === "cancelled" ? "cancelled"
        : "ongoing";

      const cover = coverUrl(s.multimedia ?? s.covers ?? []);
      const genres = mapGenres(s.genres ?? []);
      const startYear = s.start_year ?? s.year ?? null;
      const desc = (s.description ?? s.description_it ?? "").replace(/'/g, "''").slice(0, 500);

      sql.push(`insert into series (title, title_it, type, publisher_id, description_it, status, genre, start_year, slug, cover_url, is_verified)`);
      sql.push(`select ${sqlStr(title)}, ${sqlStr(titleIt)}, '${type}',`);
      sql.push(`  (select id from publishers where slug = '${pubSlug}'),`);
      sql.push(`  ${sqlStr(desc) === "''" ? "NULL" : sqlStr(desc)},`);
      sql.push(`  '${status}', ${genres}, ${startYear ?? "NULL"}, '${slug}',`);
      sql.push(`  ${cover ? sqlStr(cover) : "NULL"}, true`);
      sql.push(`where not exists (select 1 from series where slug = '${slug}');`);
      sql.push("");

      seriesInserted.push({ id: s.id, slug, title, pubSlug });
    }
  }

  // Fetch volumes for each series
  if (seriesInserted.length > 0) {
    sql.push("-- ─────────────────────────────────────────────");
    sql.push("-- VOLUMES");
    sql.push("-- ─────────────────────────────────────────────");
    sql.push("");

    // Limit to first 200 series to keep migration manageable
    const toProcess = seriesInserted.slice(0, 200);
    for (const s of toProcess) {
      console.log(`\n📦 Fetching volumes for "${s.title}"…`);
      let volumes;
      try {
        volumes = await fetchAllPages("/comics/paginated", { series_id: s.id }, 100);
      } catch (e) {
        console.warn(`  Skipping volumes for "${s.title}": ${e.message}`);
        continue;
      }

      for (const v of volumes) {
        const num = v.number ?? v.issue_number ?? v.volume_number;
        if (!num) continue;
        const cover = coverUrl(v.multimedia ?? v.covers ?? []);
        const isbn = v.isbn ?? v.ean ?? null;
        const releaseDate = v.release_date ?? v.release_date_it ?? null;
        const price = v.cover_price ?? v.price ?? null;
        const title = (v.title ?? "").replace(/'/g, "''");

        sql.push(`insert into volumes (series_id, number, title, cover_url, release_date_it, cover_price_eur, isbn)`);
        sql.push(`select s.id, ${num}, ${title ? sqlStr(title) : "NULL"},`);
        sql.push(`  ${cover ? sqlStr(cover) : "NULL"},`);
        sql.push(`  ${releaseDate ? sqlStr(releaseDate.slice(0, 10)) : "NULL"},`);
        sql.push(`  ${price ?? "NULL"},`);
        sql.push(`  ${isbn ? sqlStr(isbn) : "NULL"}`);
        sql.push(`from series s where s.slug = '${s.slug}'`);
        sql.push(`on conflict (series_id, number) do nothing;`);
        sql.push("");
        totalVolumes++;
      }
    }
  }

  const output = sql.join("\n");
  const outPath = join(process.cwd(), "supabase/migrations/007_comixtime_import.sql");
  writeFileSync(outPath, output, "utf8");

  console.log(`\n✅ Done!`);
  console.log(`   Publishers: ${italianPubs.length}`);
  console.log(`   Series: ${seriesInserted.length}`);
  console.log(`   Volumes: ${totalVolumes}`);
  console.log(`   Output: supabase/migrations/007_comixtime_import.sql`);
  console.log(`\nRun the SQL file in your Supabase SQL Editor.`);
  console.log(`Also add to next.config.ts remotePatterns:`);
  console.log(`  { protocol: "https", hostname: "fra1.digitaloceanspaces.com" }`);
}

main().catch(e => {
  console.error("Fatal:", e);
  process.exit(1);
});
