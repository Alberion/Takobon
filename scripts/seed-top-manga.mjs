#!/usr/bin/env node
/**
 * Seed top 500 manga from Jikan API (MyAnimeList)
 * Usage: node scripts/seed-top-manga.mjs
 * Output: supabase/migrations/008_top_manga.sql
 *
 * Uses the /top/manga paginated endpoint — only ~20 API calls for 500 series.
 * Volume covers: uses series cover (Jikan has no per-volume covers).
 */

import { writeFileSync } from "fs";
import { join } from "path";

const JIKAN = "https://api.jikan.moe/v4";
const TOTAL = 500;
const PER_PAGE = 25;
const PAGES = Math.ceil(TOTAL / PER_PAGE);

function sleep(ms) {
  return new Promise(r => setTimeout(r, ms));
}

function sqlStr(val) {
  if (val === null || val === undefined) return "NULL";
  return "'" + String(val).replace(/'/g, "''") + "'";
}

function slugify(str) {
  return str
    .toLowerCase()
    .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 80);
}

async function fetchPage(page, retries = 4) {
  for (let attempt = 0; attempt < retries; attempt++) {
    const res = await fetch(`${JIKAN}/top/manga?page=${page}&limit=${PER_PAGE}`);
    if (res.status === 429) {
      const wait = 4000 * (attempt + 1);
      console.log(`  Rate limited page ${page}, waiting ${wait}ms...`);
      await sleep(wait);
      continue;
    }
    if (!res.ok) throw new Error(`Jikan page ${page}: ${res.status}`);
    const json = await res.json();
    return json.data ?? [];
  }
  throw new Error(`Page ${page}: too many retries`);
}

const GENRE_MAP = {
  "Action": "Action", "Adventure": "Adventure", "Comedy": "Comedy",
  "Drama": "Drama", "Fantasy": "Fantasy", "Horror": "Horror",
  "Mystery": "Mystery", "Romance": "Romance", "Sci-Fi": "Sci-Fi",
  "Slice of Life": "Slice of Life", "Sports": "Sports", "Supernatural": "Supernatural",
  "Thriller": "Thriller", "Mecha": "Mecha", "Psychological": "Psychological",
  "Historical": "Historical", "Martial Arts": "Martial Arts", "School": "School",
  "Shounen": "Shounen", "Shoujo": "Shoujo", "Seinen": "Seinen", "Josei": "Josei",
  "Isekai": "Isekai",
};

async function main() {
  const sql = [];
  sql.push("-- Takobon — Top 500 manga from MyAnimeList via Jikan API");
  sql.push("-- Auto-generated by scripts/seed-top-manga.mjs");
  sql.push("");

  const usedSlugs = new Set();
  let inserted = 0;

  for (let page = 1; page <= PAGES; page++) {
    console.log(`Fetching page ${page}/${PAGES}...`);

    let items;
    try {
      items = await fetchPage(page);
    } catch (e) {
      console.warn(`  Skipped page ${page}: ${e.message}`);
      await sleep(3000);
      continue;
    }

    for (const manga of items) {
      const titleEn = manga.title_english ?? manga.title;
      const titleJp = manga.title;

      let slug = slugify(titleEn);
      if (!slug) slug = `manga-${manga.mal_id}`;

      // Deduplicate slugs
      if (usedSlugs.has(slug)) {
        slug = `${slug}-${manga.mal_id}`;
      }
      usedSlugs.add(slug);

      const coverUrl = manga.images?.jpg?.large_image_url ?? manga.images?.jpg?.image_url ?? null;
      const volumes = manga.volumes ?? 0;
      const status = manga.status === "Finished" ? "completed"
        : manga.status === "Discontinued" ? "cancelled"
        : "ongoing";
      const startYear = manga.published?.from ? new Date(manga.published.from).getFullYear() : null;
      const synopsis = manga.synopsis
        ? manga.synopsis.replace(/\[Written by MAL Rewrite\]/g, "").replace(/\n+/g, " ").trim().slice(0, 800)
        : null;

      const genres = (manga.genres ?? [])
        .concat(manga.demographics ?? [])
        .map(g => GENRE_MAP[g.name] ?? g.name)
        .filter(Boolean)
        .slice(0, 6);

      const genreArr = genres.length > 0
        ? "ARRAY[" + genres.map(g => sqlStr(g)).join(", ") + "]::text[]"
        : "'{}'::text[]";

      sql.push(`-- ${titleEn}`);
      sql.push(`insert into series (slug, title, title_it, type, cover_url, description_it, status, start_year, genre, is_verified)`);
      sql.push(`values (`);
      sql.push(`  ${sqlStr(slug)},`);
      sql.push(`  ${sqlStr(titleEn)},`);
      sql.push(`  ${titleEn !== titleJp ? sqlStr(titleJp) : "NULL"},`);
      sql.push(`  'manga',`);
      sql.push(`  ${sqlStr(coverUrl)},`);
      sql.push(`  ${sqlStr(synopsis)},`);
      sql.push(`  '${status}',`);
      sql.push(`  ${startYear ?? "NULL"},`);
      sql.push(`  ${genreArr},`);
      sql.push(`  true`);
      sql.push(`)`);
      sql.push(`on conflict (slug) do update set`);
      sql.push(`  cover_url = excluded.cover_url,`);
      sql.push(`  description_it = excluded.description_it,`);
      sql.push(`  status = excluded.status,`);
      sql.push(`  start_year = excluded.start_year,`);
      sql.push(`  genre = excluded.genre,`);
      sql.push(`  is_verified = true;`);
      sql.push("");

      // Insert volumes
      if (volumes > 0) {
        for (let i = 1; i <= volumes; i++) {
          const releaseYear = startYear ? startYear + Math.floor((i - 1) / 4) : null;
          const releaseMonth = releaseYear ? (((i - 1) % 4) * 3 + 1) : null;
          const releaseDate = releaseYear ? `${releaseYear}-${String(releaseMonth).padStart(2, "0")}-01` : null;
          const price = 5.90;

          sql.push(`insert into volumes (series_id, number, cover_url, release_date_it, cover_price_eur)`);
          sql.push(`select id, ${i}, ${sqlStr(coverUrl)}, ${releaseDate ? sqlStr(releaseDate) : "NULL"}, ${price}`);
          sql.push(`from series where slug = ${sqlStr(slug)}`);
          sql.push(`on conflict (series_id, number) do update set cover_url = excluded.cover_url;`);
        }
        sql.push("");
      }

      inserted++;
    }

    // Jikan allows 3 req/s burst but throttles sustained — wait between pages
    if (page < PAGES) await sleep(1500);
  }

  const output = sql.join("\n");
  const outPath = join(process.cwd(), "supabase/migrations/008_top_manga.sql");
  writeFileSync(outPath, output, "utf8");

  console.log(`\n✅ Done! ${inserted} series written to supabase/migrations/008_top_manga.sql`);
  console.log("Run it in your Supabase SQL Editor.");
}

main().catch(e => { console.error(e); process.exit(1); });
